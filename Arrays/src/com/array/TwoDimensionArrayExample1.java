package com.array;

public class TwoDimensionArrayExample1 {

	public static void main(String[] args) {
		int[][] arr= {{4,5}, {7,9}};
		for (int i=0;i<arr.length;i++)
		{
			for(int j=0;j<arr[i].length;j++)
			{
				System.out.println(arr[i][j]+" ");
			}
			System.out.println();
				
		
		}

	}

}
